package models.engine;

public interface Engine {


    /**
     * Gets the object's x coordinate
     *
     * @return the x coordinate
     */
    int getX();

    /**
     * Sets the object's x coordinate
     *
     * @param x coordinate
     */
    void setX(int x);

    /**
     * Gets the object's x coordinate
     *
     * @return the y coordinate
     */

    int getY();

    /**
     * Sets the object's y coordinate
     *
     * @param y coordinate
     */
    void setY(int y);

}
